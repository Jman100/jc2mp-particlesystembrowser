function particleSystemBrowser:Lchat(args)
	if args.text == "/particles" then
		if self.enabled then
			self.enabled = false
			self.orbitcam:Destroy()
			self.orbitcam = nil
			self:Unload()
		else
			self.enabled = true
			self.orbitcam = OrbitCamera()
		end
	end
end

function particleSystemBrowser:KeyDown(args)
	if self.enabled then
		if args.key == 39 then -- Navigate to next particle
			if self.pid == 2600 then
				self.pid = 1
			else
				self.pid = self.pid + 1
			end
			self:fireParticle(self.pid)
		elseif args.key == 32 then -- Fire current particle
			self:fireParticle(self.pid)
		elseif args.key == 37 then -- Navigate to previous particle
			if self.pid == 1 then
				self.pid = 2600
			else
				self.pid = self.pid - 1
			end
			self:fireParticle(self.pid)
		elseif args.key == 38 then -- Jump forward by 100 effects
			if self.pid + 100 > 2600 then
				self.pid = 1
			else
				self.pid = self.pid + 50
			end
			self:fireParticle(self.pid)
		elseif args.key == 40 then -- Jump backward by 100 effects
			if self.pid - 100 < 1 then
				self.pid = 1
			else
				self.pid = self.pid - 50
			end
			self:fireParticle(self.pid)
		end
	end
end
function particleSystemBrowser:fireParticle(pid)
	if self.particle ~= nil then
		self.particle:Remove()
		self.particle = nil
	end
	self.particle = ClientParticleSystem.Create(AssetLocation.Game, {
		path = self.particlesystemarray[pid],
		position = Vector3(-6334.865234, 212.981903, -3570.047363),
		angle = Angle()
	})
end

function particleSystemBrowser:Rend()
	if self.enabled then
		local name = self.particlesystemarray[self.pid]
		local text = "Number: " .. self.pid .. "/2600  Path: " .. name
		local text2 = "Left/Right to change, Up/Down to change by 100, Space to re-fire"
		
		local pos = Vector2(Render.Width / 2 - (Render:GetTextWidth(text, TextSize.Large) / 2), Render.Height - 100 - Render:GetTextHeight(text2, TextSize.Large))
		local pos2 = Vector2(Render.Width / 2 - (Render:GetTextWidth(text2, TextSize.Large) / 2), Render.Height - 100)
		Render:DrawText(pos + Vector2(1,1), text, Color(0,0,0), TextSize.Large)
		Render:DrawText(pos2 + Vector2(1,1), text2, Color(0,0,0), TextSize.Large)
		Render:DrawText(pos, text, Color(255,255,255), TextSize.Large)
		Render:DrawText(pos2, text2, Color(255,255,255), TextSize.Large)
	end
end

function particleSystemBrowser:Unload()
	if self.particle ~= nil then
		self.particle:Remove()
		self.particle = nil
	end
end
